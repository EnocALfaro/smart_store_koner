    private void btn_inicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inicioActionPerformed
    
     if (timerBloqueo != null && timerBloqueo.isRunning()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Debes esperar a que termine el conteo.");
        return;
    }

    String usuario = txt_nombre.getText().trim();
    String contraseña = new String(psw_contraseña.getPassword());

    if (usuario.equals("admin") && contraseña.equals("1234")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Inicio de sesión exitoso");

        javax.swing.JFrame nuevaVentana = new javax.swing.JFrame("Bienvenido");
        nuevaVentana.setSize(400, 300);
        nuevaVentana.setDefaultCloseOperation(javax.swing.JFrame.EXIT_ON_CLOSE);
        nuevaVentana.setLocationRelativeTo(null);
        nuevaVentana.setVisible(true);

        java.awt.Window window = javax.swing.SwingUtilities.getWindowAncestor(this);
        if (window != null) {
            window.dispose();
        }
    } else {
        intentosRestantes--;

        if (intentosRestantes > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos. Te quedan " + intentosRestantes + " intentos.");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Demasiados intentos. Debes esperar 2 minutos.");
            bloquearLogin();
        }
    }
    }//GEN-LAST:event_btn_inicioActionPerformed

