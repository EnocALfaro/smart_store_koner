    private void bloquearLogin() {
    btn_inicio.setEnabled(false);
    segundosRestantes = 120; // 2 minutos
    lblCronometro.setText("Espera: 2:00 minutos");

    timerBloqueo = new Timer(1000, new java.awt.event.ActionListener() {
        @Override
        public void actionPerformed(java.awt.event.ActionEvent e) {
            segundosRestantes--;

            int minutos = segundosRestantes / 60;
            int segundos = segundosRestantes % 60;
            lblCronometro.setText(String.format("Espera: %d:%02d minutos", minutos, segundos));

            if (segundosRestantes <= 0) {
                timerBloqueo.stop();
                btn_inicio.setEnabled(true);
                intentosRestantes = 3;
                lblCronometro.setText("");
                javax.swing.JOptionPane.showMessageDialog(null, "Ya puedes volver a intentarlo.");
            }
        }
    });

    timerBloqueo.start();
}

    
    
    
    
    
   
   
    
